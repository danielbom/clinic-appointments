import { Path } from '../lib/path'
import { type Writable } from '../lib/writable'
import openApiJson from '../public/api/openapi.json' with { type: 'json' }

function generateBunRoutes(w: Writable) {
  w.write('// Code generated by generate-bun-routes.ts. DO NOT EDIT.\n\n')
  w.write("import { bunResolversAdapter } from './adapter'\n")
  w.write('\n')
  w.write('export const routes = {\n')
  for (const url in openApiJson.paths) {
    const bunUrl = url.replaceAll(/\{(\w+)\}/g, ':$1')
    w.write(`  '${bunUrl}': {\n`)
    const path = (openApiJson.paths as any)[url]
    for (const method in path) {
      const endpoint = path[method]
      w.write(`    ${method.toUpperCase()}: bunResolversAdapter('${endpoint.operationId}'),\n`)
    }
    w.write('  },\n')
  }
  w.write("} satisfies Bun.Serve.Options<any, any>['routes']\n")
}

const routesPath = Path.from(import.meta.dirname)
  .parent()
  .append('routes.ts')

const file = routesPath.open('w')
try {
  generateBunRoutes(file)
  console.log(`INFO: ${routesPath} generated`)
} finally {
  file.close()
}
